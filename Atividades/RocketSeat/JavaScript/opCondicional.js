// If
let hora = 19

if(hora>=12 && hora < 18){
    console.log("Boa tarde")
}

//If Else
if(hora>=4 && hora < 12){
    console.log("Bom dia")
}
else{
    console.log("Boa tarde")
}

// If Else If

if(hora>=4 && hora < 12){
    console.log("Bom dia")
}
else if(hora>=12 && hora < 18){
    console.log("Boa tarde")
}
else{
    console.log("Boa noite")
}